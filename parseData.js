var parseData = function(arr){
	var temp = '';

	// Add an property on the object called start. 
	for(var j =0;j<arr.length;j++){
		aggregate(arr[j]);
	} 

	
	for(var i =0;i<arr.length;i++){
		temp = arr[i].date;
		arr[i].start.local = temp;
		delete arr[i].date;
	}

	for(var i = 0;i<arr.length;i++){
		// find the index of '-' in the date value, assign the to a var DASH
		var dashString = arr[i].start.local + '';
		

		var dashIndex = dashString.indexOf('–'); // – is a special dash call en dash. ascii 8211
		// console.log('en dash, are you here?', arr[i].start.local.indexOf('–'));
		var strLen = arr[i].start.local.length-1;

		// assign the time string to the index DASH+1 to send of string
		arr[i].time = arr[i].start.local.substr(dashIndex+1, strLen);
		
		// assign the start string to the index 0-DASH-1
		arr[i].start.local = arr[i].start.local.substr(0, dashIndex-1);

	};
	

	return arr;
};

var aggregate = function(obj){
	obj.start = {};
	obj.time = {};
	return obj;
};

